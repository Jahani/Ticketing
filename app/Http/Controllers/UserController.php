    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, User $user)
    {
        $this->validate($request, [
            'name' => ['required', 'string', 'max:255'],
            'password' => ['string', 'nullable', 'min:6', 'confirmed'],
            'organizer_name' => 'nullable|string|max:180',
            'organizer_tel' => 'nullable|string|max:180',
        ]);

        // User can't change his email
        $user->fill($request->except(['email', 'password']));
        if (strlen($request->input('password'))) {
            $user->password = Hash::make($request->input('password'));
        }
        $user->save();

        session()->flash('message', __('Profile has been successfully updated.'));

        return back();
    }

